SELECT
  WPESSOA.ID            AS COD_PROFESSOR_EXT,
  WPESSOA.NOME          AS NOM_PROFESSOR,
  WPESSOA.CPF           AS CPF_PROFESSOR,
  WPESSOA.SENHA         AS SEN_PROFESSOR,
  WPESSOA.USUARIO       AS LOG_PROFESSOR,
  WPESSOA.EMAIL1        AS EML_PROFESSOR,
  WPESSOA.RGRNE         AS NUM_IDENTIDADE,
  WPESSOA.RGRNEDT       AS DAT_EMISSAO,
  WPESSOA.RGRNEEMISSOR  AS NOM_ORG_EXP_CI,
  WPESSOA.FONECEL       AS TEL_CELULAR,
  SUBSTR(SEXO.NOME,1,1) AS IND_SEXO,
  WPESSOA.DTNASCTO      AS DAT_NASCIMENTO,
  DECODE(admissao_gnAtivo(WPessoa.Id,trunc(sysdate)),0,2,1) AS COD_STA_PROFESSOR
FROM
  WPESSOA,
  SEXO
WHERE
  DOCENTE='on'
AND
  WPESSOA.SEXO_ID=SEXO.ID(+)
ORDER BY 2