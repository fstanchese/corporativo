SELECT
  TO_CHAR(PLETIVO.ID)||TO_CHAR(CURSO.CURSONIVEL_ID) AS COD_PERIODO_LETIVO_EXT,
  SUBSTR(PLETIVO.NOME,1,4) AS ANO,
  NULL                     AS SEMESTRE,
  DECODE(PLETIVO.CICLO_ID,5400000000001,2,5400000000002,1)  AS REGIME,
  PLETIVO.NOME             AS DSC_PERIODO_LETIVO,
  PLETIVO.INICIO           AS DAT_INICIO,
  PLETIVO.FINAL            AS DAT_FINAL,
  NULL                     AS DAT_INICIO_AULA,
  NULL                     AS DAT_FINAL_AULA,
  PLETIVO.NOME             AS COD_CLD_ESCOLAR_EXT,
  DECODE(CURSO.CURSONIVEL_ID,6200000000001,1,6200000000002,2,6200000000008,3,6200000000010,5,6200000000012,5,6200000000015,19) AS COD_TPO_CURSO,
  NULL                     AS COD_CURSO_EXT
FROM
  PLETIVO,CURROFE,CURR,CURSO
WHERE
  CURR.CURSO_ID=CURSO.ID
AND
  CURROFE.CURR_ID=CURR.ID
AND
  CURROFE.PLETIVO_ID=PLETIVO.ID
GROUP BY PLETIVO.ID,CURSO.CURSONIVEL_ID,PLETIVO.NOME,PLETIVO.CICLO_ID,PLETIVO.INICIO,PLETIVO.FINAL

