SELECT
  'BRA'        AS COD_PAIS_EXT,
  'BRASIL'     AS NOM_PAIS,
  'BRA'        AS SGL_PAIS,
  ESTADO.ID    AS COD_ESTADO_EXT,
  ESTADO.NOME  AS NOM_ESTADO,
  ESTADO.SIGLA AS SGL_ESTADO,
  CIDADE.ID    AS COD_CIDADE_EXT,
  CIDADE.NOME  AS NOM_CIDADE
FROM 
   CIDADE,
   ESTADO
WHERE
   CIDADE.ESTADO_ID=ESTADO.ID   
   
  
